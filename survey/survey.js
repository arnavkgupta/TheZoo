var sportsButton = document.getElementById("sportsButton");
var isClicked = false; // Store the clicked state

sportsButton.addEventListener("click", function() {
  if (isClicked) {
    // Revert to the original color
    sportsButton.style.backgroundColor = "rgba(124,174,76,0.28999999165534973)";
    sportsButton.style.color = "black";
  } else {
    // Change to a new color
    var randomColor = "#4caf50";
    sportsButton.style.backgroundColor = randomColor;
    sportsButton.style.color = "black";
  }
  isClicked = !isClicked; // Toggle the clicked state
});

